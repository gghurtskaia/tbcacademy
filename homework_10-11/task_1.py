n = int(input("შეიყვანეთ 1-ზე მეტი, დადებითი მთელი რიცხვი: "))

while n <= 1:
    print("შეყვანილია არასწორი რიცხვი")
    n = int(input("შეიყვანეთ 1-ზე მეტი, დადებითი მთელი რიცხვი: "))
else:
    x = 0
    sign = 1
    for i in range(n):
        x += sign / (2 * i + 1)
        sign *= -1
    x *= 4
    print(f"x-ის მნიშვნელობა როცა n = {n} არის: {x}")
#არ ვიცი პირობა რამდენად სწორად გავიგე. ამ პროგრამით pi-ს მიახლოებით რიცხვს გვიბრუნებს. რაც მეტია n მით უფრო მიახლოებულია pi-ს მნიშვნელობასთან.
